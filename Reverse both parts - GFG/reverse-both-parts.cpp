class Solution
{
public:
   
    Node *reverse(Node *head, int k)
    {
        // code here
        Node *s=head;
        Node *p=head;
        Node *q=NULL;
        Node *r=NULL;
        while(k--)
        {
            r=q;
            q=p;
            p=p->next;
            q->next=r;
        }
        Node *x=q;
        q=NULL;
        r=NULL;
        while(p)
        {
            r=q;
            q=p;
            p=p->next;
            q->next=r;
        }
        s->next=q;
        return x;
    }
};

